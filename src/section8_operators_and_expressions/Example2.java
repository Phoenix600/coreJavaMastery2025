package section8_operators_and_expressions;

// Demonstration On Arithmetic Operators
public class Example2 {
    public static void main(String[] args) {

        // Integer Type Data
        int a = 12;
        int b = 5;

        // Division Operator
        int q = a/b;

        // Modulo Operator
        int r = a % b;

        System.out.println(q);
        System.out.println(r);


        // Float Data Type
        float lfNum1 = 14.4f;
        float lfNum2 = 3.2f;
        float lfRem  = lfNum1 % lfNum2; // We can perform Mod (%) operation on float data type

        System.out.println(lfRem);



    }
}
